
/* array functions:

- .push()
- .pop()
- .shift()
- .unshift()

*/


// ORIGINAL ARRAY
var myNewArray = ["Spimsons", "H", "Dog"];
console.log("the original array is: " + myNewArray)


// PUSH FUNCTION
myNewArray.push(["happy", "Joy"])
myNewArray.push("Cat")

console.log(myNewArray)


// POP FUNCTION

myNewArray.pop();
console.log(myNewArray)


// SHIFT FUNCTION
myNewArray.shift();
console.log(myNewArray)


// UNSHIFT FUNCTION
myNewArray.unshift("Leonidas")
console.log(myNewArray)